#include<bits/stdc++.h>
using namespace std;
#define endl '\n'
#define int long long
#define all(a) a.begin(),a.end()
#define pb push_back
#define mod 1000000007
bool marked[10000005];
vector <int> primes;

void sieve()
{
    int i,j,mx=10000000;
    primes.pb(2);

    for(i=3; i*i<=mx; i+=2)
    {
        if(marked[i]==false)
        {
            for(j=i*i; j<=mx; j+=(i+i))
            {
                marked[j] = true;
            }
        }
    }

    for(i=3; i<=mx; i+=2)
    {
        if(marked[i]==false)
        {
            primes.pb(i);
        }
    }

}

void PrimeFactorization(int x)
{
    int i,j,cn=0;

    for(i=0; primes[i]*primes[i]<=x; i++)
    {
        cn=0;
        while(x%primes[i]==0)
        {
            cn++;
            x/=primes[i];
        }
        if(cn)
        {
            cout << primes[i] << " " << cn << endl;
        }
    }

    if(x>1)
    {
        cout << x << " " << 1 << endl;
    }
}

void solve()
{
    int x;
    cin >> x;
    PrimeFactorization(x);
}

int32_t main()
{
    ios_base::sync_with_stdio(0);
    cin.tie(0);
    cout.tie(0);

    sieve();
    int t=1;
    while(t--)
    {
        solve();
    }
}
